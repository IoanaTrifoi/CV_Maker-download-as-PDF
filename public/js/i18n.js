// ── Detectare limbă ──────────────────────────────────────────────
const SUPPORTED = Object.keys(I18N_LANGS);

function detectLang() {
  // 1. localStorage (alegere anterioară)
  const stored = localStorage.getItem('cv_lang');
  if (stored && SUPPORTED.includes(stored)) return stored;
  // 2. navigator.language (limba browserului)
  const nav = (navigator.language || '').toLowerCase().split('-')[0];
  if (SUPPORTED.includes(nav)) return nav;
  // 3. fallback romanian
  return 'ro';
}

let _lang = detectLang();

function t(key, vars = {}) {
  const dict = I18N[_lang] || I18N.ro;
  let str = dict[key] || I18N.ro[key] || key;
  Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
  return str;
}

function setLang(lang) {
  if (!SUPPORTED.includes(lang)) return;
  _lang = lang;
  localStorage.setItem('cv_lang', lang);
  applyTranslations();
  // Re-render dacă editorul e inițializat
  if (typeof renderCanvas === 'function') { renderCanvas(); renderProps(); }
  if (typeof renderTemplateGridMenu === 'function') { renderTemplateGridMenu(); }
  document.documentElement.lang = lang;
}

function getLang() { return _lang; }

// ── Aplică traduceri pe elementele HTML statice ──────────────────
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    const attr = el.dataset.i18nAttr; // 'title', 'placeholder', etc.
    const val = t(key);
    if (attr) el.setAttribute(attr, val);
    else el.textContent = val;
  });
  // Actualizează titlul ferestrei
  document.title = 'CV Builder';
}

// ── Modal selector limbă (la prima deschidere) ───────────────────
function showLangPickerIfNeeded() {
  if (localStorage.getItem('cv_lang')) return; // deja ales
  const modal = document.getElementById('langPickerModal');
  if (modal) modal.style.display = 'flex';
}

function initLangPicker() {
  const modal = document.getElementById('langPickerModal');
  if (!modal) return;

  const btnsWrap = modal.querySelector('.lang-picker-btns');
  if (btnsWrap) {
    btnsWrap.innerHTML = SUPPORTED.map((code) => {
      const info = I18N_LANGS[code];
      return `<button class="lang-pick-btn${_lang === code ? ' active' : ''}" data-lang="${code}">
        <span class="lang-flag">${info.flag}</span>
        <span class="lang-name">${info.label}</span>
      </button>`;
    }).join('');
    btnsWrap.querySelectorAll('.lang-pick-btn').forEach((btn) => {
      btn.addEventListener('click', () => {
        setLang(btn.dataset.lang);
        modal.style.display = 'none';
        // Actualizează butonul activ
        btnsWrap.querySelectorAll('.lang-pick-btn').forEach((b) => b.classList.remove('active'));
        btn.classList.add('active');
      });
    });
  }

  modal.querySelector('.lang-picker-close')?.addEventListener('click', () => {
    localStorage.setItem('cv_lang', _lang); // salvează ce e detectat
    modal.style.display = 'none';
  });
}

// ── Menu language switcher ───────────────────────────────────────
function buildLangMenuBtn() {
  const menubar = document.querySelector('.menubar-right');
  if (!menubar) return;

  const wrap = document.createElement('div');
  wrap.className = 'menu-item lang-switcher';
  wrap.innerHTML = `
    <span class="menu-label lang-btn-label">
      <span class="lang-flag-small">${I18N_LANGS[_lang].flag}</span>
      ${I18N_LANGS[_lang].label}
    </span>
    <div class="menu-dropdown lang-dropdown">
      ${SUPPORTED.map((code) => {
        const info = I18N_LANGS[code];
        return `<button class="menu-drop-btn lang-opt${_lang === code ? ' active' : ''}" data-lang="${code}">
          ${info.flag} ${info.label}
        </button>`;
      }).join('')}
    </div>`;

  wrap.querySelectorAll('.lang-opt').forEach((btn) => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      setLang(btn.dataset.lang);
      // Actualizează label
      const lbl = wrap.querySelector('.lang-btn-label');
      if (lbl) lbl.innerHTML = `<span class="lang-flag-small">${I18N_LANGS[_lang].flag}</span> ${I18N_LANGS[_lang].label}`;
      wrap.querySelectorAll('.lang-opt').forEach((b) => b.classList.remove('active'));
      btn.classList.add('active');
    });
  });

  // Inserează înaintea span-ului #status
  const statusSpan = menubar.querySelector('#status');
  menubar.insertBefore(wrap, statusSpan || null);
}

// ── Init ─────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  initLangPicker();
  buildLangMenuBtn();
  showLangPickerIfNeeded();
  document.documentElement.lang = _lang;
});
